package DrinkStore;

public class Ice {
	private int select;
	private String ice;

	public Ice(int i, String string) {
		select = i;
		ice = string;
	}

	public int getSelect() {
		return select;
	}

	public void setSelect(int select) {
		this.select = select;
	}

	public String getIce() {
		return ice;
	}

	public void setIce(String ice) {
		this.ice = ice;
	}

	

}
