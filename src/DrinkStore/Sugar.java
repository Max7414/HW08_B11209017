package DrinkStore;

public class Sugar {
	private int select;
	private String sugar;

	public Sugar(int i, String string) {
		select = i;
		sugar = string;
	}

	public int getSelect() {
		return select;
	}

	public void setSelect(int select) {
		this.select = select;
	}

	public String getSugar() {
		return sugar;
	}

	public void setSugar(String sugar) {
		this.sugar = sugar;
	}

}
